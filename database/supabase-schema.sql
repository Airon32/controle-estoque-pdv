-- Política pública para categorias
CREATE POLICY "Anyone can read categories" ON categories
    FOR SELECT USING (true);

CREATE POLICY "Authenticated can manage categories" ON categories
    FOR ALL USING (auth.role() = 'authenticated');

-- Política pública para fornecedores
CREATE POLICY "Anyone can read suppliers" ON suppliers
    FOR SELECT USING (true);

CREATE POLICY "Authenticated can manage suppliers" ON suppliers
    FOR ALL USING (auth.role() = 'authenticated');

-- Política pública para produtos
CREATE POLICY "Anyone can read products" ON products
    FOR SELECT USING (active = true);

CREATE POLICY "Authenticated can manage products" ON products
    FOR ALL USING (auth.role() = 'authenticated');

-- Política para vendas
CREATE POLICY "Authenticated can manage sales" ON sales
    FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Anyone can read sales" ON sales
    FOR SELECT USING (true);

-- Política para itens de venda
CREATE POLICY "Authenticated can manage sale_items" ON sale_items
    FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Anyone can read sale_items" ON sale_items
    FOR SELECT USING (true);

-- Política para movimentações de estoque
CREATE POLICY "Anyone can read stock_movements" ON stock_movements
    FOR SELECT USING (true);

CREATE POLICY "Authenticated can manage stock_movements" ON stock_movements
    FOR ALL USING (auth.role() = 'authenticated');

-- Política para perfis
CREATE POLICY "Users can read own profile" ON profiles
    FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Authenticated can manage profiles" ON profiles
    FOR ALL USING (auth.role() = 'authenticated');

